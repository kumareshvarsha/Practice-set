public class InterviewBit{
    public int num1;
    public int num2;
    InterviewBit(int num1, int num2){
        num1=num1;
        num2=num2;
    }
    public static void main(String[] args) {
        InterviewBit interviewBit=new InterviewBit(20, 040);
        System.out.println(interviewBit.num1);
        System.out.println(interviewBit.num2);
    }
}